#include "libft.h"

int	ft_putstrn_fd(char *s, int fd, size_t n)
{
	size_t	i;

	if (!s)
		return (0);
	i = 0;
	while (s[i] && i < n)
		write(fd, &s[i++], 1);
	return (i);
}
